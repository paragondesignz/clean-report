// Utility function to generate avatar initials from name or email
export const getAvatarFallback = (name?: string, email?: string): string => {
  if (name) {
    const nameParts = name.trim().split(' ')
    if (nameParts.length >= 2) {
      return `${nameParts[0][0]}${nameParts[nameParts.length - 1][0]}`.toUpperCase()
    }
    return nameParts[0][0].toUpperCase()
  }
  
  if (email) {
    return email[0].toUpperCase()
  }
  
  return 'U'
}

// Utility function to get consistent avatar background color based on string
export const getAvatarColorClasses = (str?: string): string => {
  if (!str) return 'bg-blue-100 text-blue-600'
  
  const colorClasses = [
    'bg-blue-100 text-blue-600',
    'bg-green-100 text-green-600', 
    'bg-purple-100 text-purple-600',
    'bg-orange-100 text-orange-600',
    'bg-pink-100 text-pink-600',
    'bg-indigo-100 text-indigo-600',
    'bg-red-100 text-red-600',
    'bg-yellow-100 text-yellow-600'
  ]
  
  let hash = 0
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash)
  }
  
  return colorClasses[Math.abs(hash) % colorClasses.length]
} 